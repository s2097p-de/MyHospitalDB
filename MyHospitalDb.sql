-- STEP 1: Create Database & Use Database
CREATE DATABASE MyHospitalDB;

USE MyHospitalDB;

-- STEP 2: Create Tables 
-- Doctor
CREATE TABLE Doctor (
    DoctorID INT PRIMARY KEY,
    Doctor_Name VARCHAR(100),
    Dept VARCHAR(50),
    Doctor_Type VARCHAR(20)
);

-- Patient
CREATE TABLE Patient (
    Pid INT PRIMARY KEY,
    F_Name VARCHAR(50),
    L_Name VARCHAR(50),
    Age INT,
    Gender VARCHAR(10),
    Address VARCHAR(100),
    Disease VARCHAR(100),
    DoctorID INT
);

-- Room
CREATE TABLE Room (
    Room_No INT PRIMARY KEY,
    Room_Type VARCHAR(50),
    Status VARCHAR(20)
);

-- Inpatient
CREATE TABLE Inpatient (
    Pid INT,
    Room_No INT,
    Date_of_Admission DATE,
    Date_of_Discharge DATE,
    Advance DECIMAL(10,2)
);

-- Outpatient
CREATE TABLE Outpatient (
    Pid INT,
    Visit_Date DATE,
    DoctorID INT
);

-- Lab Report
CREATE TABLE Lab_Report (
    Lab_No INT PRIMARY KEY,
    Pid INT,
    Category VARCHAR(50),
    Amount DECIMAL(10,2),
    Date DATE,
    DoctorID INT
);

-- Bill
CREATE TABLE Bill (
    Bill_No INT PRIMARY KEY,
    Pid INT,
    Room_Charges DECIMAL(10,2),
    Lab_Charges DECIMAL(10,2),
    Medicine_Charges DECIMAL(10,2),
    Operation_Charges DECIMAL(10,2),
    No_of_Days INT
);


-- STEP 3: Add Foreign Keys
ALTER TABLE Patient
ADD CONSTRAINT FK_Patient_Doctor
FOREIGN KEY (DoctorID) REFERENCES Doctor(DoctorID);

ALTER TABLE Inpatient
ADD FOREIGN KEY (Pid) REFERENCES Patient(Pid);

ALTER TABLE Outpatient
ADD FOREIGN KEY (Pid) REFERENCES Patient(Pid);

ALTER TABLE Lab_Report
ADD FOREIGN KEY (Pid) REFERENCES Patient(Pid);

ALTER TABLE Bill
ADD FOREIGN KEY (Pid) REFERENCES Patient(Pid);

